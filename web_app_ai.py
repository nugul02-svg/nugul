    # 능률이 떨어진 결과(사회적 억제)가 드러나야 함
    if not any(w in a2 for w in ['실수', '못', '틀렸', '떨어', '안 됐', '낮아', '망', '엉망', '흔들', '굳어', '잊어', '멈추', '버벅']):
        f.append("(2)번: 사회적 억제 경험이므로 타인 앞에서 능률이 '떨어지거나 실수한' 결과가 드러나야 합니다.")
