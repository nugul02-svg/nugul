    wrong_condition = any(w in a2 for w in ['잘하는', '자신 있', '자신있', '익숙한', '잘하던', '쉬운', '좋아하는 과목'])
    correct_condition = any(w in a2 for w in ['익숙하지', '어렵', '못하', '낯선', '처음', '연습이 부족', '어설프', '도전'])
    if wrong_condition and not correct_condition:
        f.append("(2)번: 사회적 억제는 '익숙하지 않거나 어려운 과제'를 할 때 발생합니다. 잘하거나 자신 있는 과제의 경험은 사회적 억제가 아닌 사회적 촉진에 해당할 수 있으므로 경험을 다시 점검해 보세요.")
